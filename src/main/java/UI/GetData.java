    private void resize() {
        int width = Integer.parseInt(WidthTextField.getText());
        int height = Integer.parseInt(HeightTextField.getText());
        if (OriginalSize.isSelected()) {
            width = originalImage.getWidth();
            height = originalImage.getHeight();
        }
        resizedImage = new BufferedImage(width, height, originalImage.getType());
        Graphics2D g2d = resizedImage.createGraphics();
        g2d.drawImage(originalImage, 0, 0, width, height, null);
        g2d.dispose();
    }
    
    public static BufferedImage getBufferedImageFromJson(String base64Image) throws IOException {
        byte[] imageBytes = Base64.getDecoder().decode(base64Image); // Decode Base64

        // Change into BufferedImage
        try (ByteArrayInputStream bis = new ByteArrayInputStream(imageBytes)) {
            return ImageIO.read(bis);
        }
    }
    
    private void BrowseFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BrowseFileActionPerformed
        // TODO add your handling code here:
        
        
        JFileChooser fileChooser = new JFileChooser();
        // File Filter
        fileChooser.setFileFilter(new javax.swing.filechooser.FileFilter() {
            @Override
            public boolean accept(File f) {
                if (f.isDirectory()) {
                    return true;
                }
                String name = f.getName().toLowerCase();
                return name.endsWith(".jpg") || name.endsWith(".jpeg") || 
                       name.endsWith(".png") || name.endsWith(".gif") || 
                       name.endsWith(".bmp");
            }

            @Override
            public String getDescription() {
                return "Image Files (*.jpg, *.jpeg, *.png, *.gif, *.bmp)";
            }
        });
        
        int result = fileChooser.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            // Selected file
            File selectedFile = fileChooser.getSelectedFile();
            PathField.setText(selectedFile.getAbsolutePath());
            try { 
                originalImage = ImageIO.read(selectedFile);
                resize();
            } catch (IOException ex) {
                Logger.getLogger(GetData.class.getName()).log(Level.SEVERE, null, ex);
            }
            // Check for file
            if (selectedFile.exists() && selectedFile.isFile()) {
                // 
                System.out.println("Valid file!");
            } else {
                System.out.println("Not a valid file.");
            }
        } else {
            System.out.println("No valid file.");
        }
        
        
    }//GEN-LAST:event_BrowseFileActionPerformed

