    private static String bufferedImageToBase64(BufferedImage image) throws IOException {
        java.io.ByteArrayOutputStream outputStream = new java.io.ByteArrayOutputStream();
        ImageIO.write(image, "png", outputStream);
        byte[] imageBytes = outputStream.toByteArray();
        return Base64.getEncoder().encodeToString(imageBytes);
    }
    
    private void ApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApplyActionPerformed
        if (mode == 0) {
            if (resizedImage != null) {
                int width = Integer.parseInt(WidthTextField.getText());
                int height = Integer.parseInt(HeightTextField.getText());
                if (OriginalSize.isSelected()) {
                    width = originalImage.getWidth();
                    height = originalImage.getHeight();
                }   
                String base64Image = null;
                try {
                    base64Image = bufferedImageToBase64(resizedImage);
                } catch (IOException ex) {
                    Logger.getLogger(GetData.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (SaveAsJSON.isSelected()) {
                    JSONObject image = new JSONObject();
                    image.put("data", base64Image); // Example base64 string
                    image.put("type", "png");
                    image.put("height", height);
                    image.put("width", width);
                    parent.updateTable(targetKey, image);
                } else {
                    parent.updateTable(targetKey, base64Image);
                }
            }
        } else if (mode == 1) {
            parent.updateTable(targetKey, jTextArea.getText());
        } else if (mode == 2) {
            int selected = NumberType.getSelectedIndex();
            Object value = null;
            if (selected == 0) {
                try {
                    value = Integer.parseInt(NumberField.getText());
                } catch (Exception e) {
                }
            } else if (selected == 1) {
                try {
                    value = Long.parseLong(NumberField.getText());
                } catch (Exception e) {
                }
            } else if (selected == 2) {
                try {
                    value = Float.parseFloat(NumberField.getText());
                } catch (Exception e) {
                }
            } else if (selected == 3) {
                try {
                    value = Double.parseDouble(NumberField.getText());
                } catch (Exception e) {
                }
            }
            parent.updateTable(targetKey, value);
        }
        this.dispose();
    }//GEN-LAST:event_ApplyActionPerformed

    private void VIewFile1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VIewFile1ActionPerformed
        // TODO add your handling code here:
        // Open file chooser to select the save location and file type
        JFileChooser saveFileChooser = new JFileChooser();
        saveFileChooser.setDialogTitle("Save Resized Image");
        saveFileChooser.setSelectedFile(new File("resized_image.jpg")); // Default name and extension
        int saveResult = saveFileChooser.showSaveDialog(null);

        if (saveResult == JFileChooser.APPROVE_OPTION) {
            File saveFile = saveFileChooser.getSelectedFile();
            String filePath = saveFile.getAbsolutePath();
            String fileExtension = "png"; // Default to JPG

            // Check if the user specified an extension
            if (filePath.contains(".")) {
                fileExtension = filePath.substring(filePath.lastIndexOf('.') + 1).toLowerCase();
            } else {
                // Add default extension if none specified
                saveFile = new File(filePath + "." + fileExtension);
            }

            try {
                // Save the resized image
                ImageIO.write(resizedImage, fileExtension, saveFile);
            } catch (IOException ex) {
                Logger.getLogger(GetData.class.getName()).log(Level.SEVERE, null, ex);
            }
            System.out.println("Resized image saved at: " + saveFile.getAbsolutePath());
        } else {
            System.out.println("Save operation canceled.");
        }
    }//GEN-LAST:event_VIewFile1ActionPerformed

